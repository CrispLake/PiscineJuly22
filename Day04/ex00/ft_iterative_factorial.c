int	ft_iterative_factorial(int nb)
{
	int	x;

	x = nb;
	if (nb < 13 && nb > 0)
	{
		while (nb != 1)
		{
			x = x * (nb - 1);
			nb--;
		}
	}
	else if (nb == 0)
	{
		return (1);
	}
	else
	{
		return (0);
	}	
	return (x);
}
